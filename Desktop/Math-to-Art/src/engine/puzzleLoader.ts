function getDotsForPuzzle(id: string): [number, number][] {
  if (dotPatterns[id]) {
    const pattern = dotPatterns[id];
    const base = bases.find(b => b.id === id);
    if (base) {
      const count = base.difficulty === 'EASY' ? 12 : base.difficulty === 'MEDIUM' ? 14 : 16;
      return pattern(count);
    }
  }
  return genPattern('circle', 12);
}

export function loadAllPuzzles(difficulty: Difficulty): Puzzle[] {
  return bases
    .filter(b => b.difficulty === difficulty)
    .map(b => attachDots(b));
}

export function getPuzzleById(id: string): Puzzle {
  const base = bases.find(x => x.id === id)!;
  return attachDots(base);
}

function attachDots(base: Omit<Puzzle,'dots'>): Puzzle {
  const arr = getDotsForPuzzle(base.id);
  // Generate questions based on puzzle difficulty
  const ds: Dot[] = arr.map(([x,y], i) => ({ 
    id: i + 1, 
    x, 
    y, 
    q: genQuestion(base.difficulty) 
  }));
  return { ...base, dots: ds };
}
